list = ['python', 'is', 'very', 'cool']
list2 = ['javascript', 'is', 'also']
print(list)  # 输出第一个list
print(list[0])  # 输出第一个list的第一个元素
print(list[1:3])  # 输出第一个list的第二个到第四个元素
print(list[2:])  # 输出第一个list的第二个到最后的所有元素
print(list[-3:])  # 输出第一个list的倒数第三个到最后的所有元素
print(list * 2)  # 输出第一个list两次
print(list + list2)  # 输出第一个list和第二个list的所有值
